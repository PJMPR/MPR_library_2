package library.dao.repositories;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;
import java.util.ArrayList;
import java.util.List;

import library.domain.Author;

public class AuthorRepository {

	Connection connection;
	private boolean tableExists;
	
	PreparedStatement insert;
    PreparedStatement selectById;
    PreparedStatement lastId;
    PreparedStatement selectByPage;
    PreparedStatement count;
  
	
	public AuthorRepository() {

		try {
			connection = DriverManager.getConnection("jdbc:hsqldb:hsql://localhost/workdb");
			
			insert = connection.prepareStatement(""
					+ "INSERT INTO author(name,secondname,surname)"
					+ "VALUES(?,?,?)");
			
			selectById = connection.prepareStatement(""
					+ "SELECT * FROM author WHERE id=?");
			
			lastId = connection.prepareStatement(""
					+ "SELECT MAX(id) FROM author"
					+ "");
			
			count = connection.prepareStatement(""
					+ "SELECT COUNT(*) FROM author"
					+ "");
			
			selectByPage = connection.prepareStatement(""
					+ "SELECT * FROM author OFFSET ? LIMIT ?"
					+ "");
			
			
			ResultSet rs = connection.getMetaData().getTables(null, null, null, null);
			
			while(rs.next()){
				if(rs.getString("TABLE_NAME").equalsIgnoreCase("author")){
					tableExists = true;
					break;
				}
			}
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
		
	}

	public int count(){
		
		try {
			ResultSet rs = count.executeQuery();
			while(rs.next())
				return rs.getInt(1);
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
		return 0;
	}
	public int lastId(){
		
		try {
			ResultSet rs = lastId.executeQuery();
			while(rs.next())
				return rs.getInt(1);
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
		return 0;
	}
	
	public List<Author> getPage(int offset, int limit){
		
		List<Author> result = new ArrayList<Author>();
		try {
			selectByPage.setInt(1, offset);
			selectByPage.setInt(2, limit);
			ResultSet rs = selectByPage.executeQuery();
			while(rs.next()){
				Author a = new Author();
				a.setId(rs.getInt("id"));
				a.setName(rs.getString("name"));
				a.setSecondName(rs.getString("secondname"));
				a.setSurname(rs.getString("surname"));
				result.add(a);
			}
		} catch (SQLException e) {
			e.printStackTrace();
		}
		return result;
	}
	
	public Author get(int id){
		Author author = null;
				
		try {
			selectById.setInt(1, id);
			ResultSet rs = selectById.executeQuery();
			while(rs.next()){
				author=new Author();
				author.setName(rs.getString("name"));
				author.setSecondName(rs.getString("secondname"));
				author.setSurname(rs.getString("surname"));
				author.setId(rs.getInt("id"));
			}
		} catch (SQLException e) {
			e.printStackTrace();
		}		
				
		return author;
		
	}
	
	public void add(Author author){
		
		try {
			insert.setString(1, author.getName());
			insert.setString(2, author.getSecondName());
			insert.setString(3, author.getSurname());
			insert.executeUpdate();
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
		
	}
	
	public void createTable(){
		
		String createTableSql = "CREATE TABLE author("
				+ "id bigint GENERATED BY DEFAULT AS IDENTITY,"
				+ "name VARCHAR(20),"
				+ "surname VARCHAR(50),"
				+ "secondname VARCHAR(20)"
				+ ")";
		
		try {
			Statement createTable = connection.createStatement();
			if(!tableExists)
				createTable.executeUpdate(createTableSql);
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
	}
	
}
