package library.dao.repositories;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

public class ReservationOrderRepository extends RepositoryBase{

	Connection connection;
	private boolean tableExists;
	
	
	public ReservationOrderRepository(Connection connection) {
		super(connection);
	}
	
	@Override
	protected String getTableName() {
		return "reservationorder";
	}

	@Override
	protected String getUpdateSql() {
		return "UPDATE reservationorder SET (userid, date)=(?,?) WHERE id=?";
	}

	@Override
	protected String getInsertSql() {
		return "INSERT INTO reservationorder(userid, date) VALUES(?,?)";
	}
	
	
	public void createTable(){
		
		String createTableSql = "CREATE TABLE reservationorder("
				+ "id bigint GENERATED BY DEFAULT AS IDENTITY,"
				+ "userid INT,"
				+ "date DATETIME)";
		
		try {
			Statement createTable = connection.createStatement();
			if(!tableExists)
			createTable.executeUpdate(createTableSql);
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
	}
	
}
