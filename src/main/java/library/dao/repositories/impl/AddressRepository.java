package library.dao.repositories.impl;

import java.sql.Connection;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;
import java.util.ArrayList;
import java.util.List;

import library.dao.mappers.IMapper;
import library.domain.Address;

public class AddressRepository extends RepositoryBase<Address>{

	public AddressRepository(Connection connection, IMapper<Address> mapper){
		super(connection, mapper);
	}
	
	@Override
	protected String createTableSql() {
		return "CREATE TABLE address("
				+ "id bigint GENERATED BY DEFAULT AS IDENTITY,"
				+ "city VARCHAR(20),"
				+ "postalcode Varchar(7),"
				+ "street VARCHAR(32),"
				+ "apnumber VARCHAR(8)"
				+ ")";
	}

	@Override
	protected String getUpdateSql() {
		return "UPDATE address SET (city, postalcode, street, apnumber)=(?,?,?,?) WHERE id=?";
	}

	@Override
	protected String getTableName() {
		return "address";
	}

	@Override
	protected String getInsertSql() {
		return "INSERT INTO address(city,postalcode,street,apnumber) VALUES(?,?,?,?)";
	}

	@Override
	protected void setUpdate(Address adress) throws SQLException {
		update.setString(1, adress.getCity());
		update.setString(2, adress.getPostalCode());
		update.setString(3, adress.getStreet());
		update.setString(4, adress.getApNumber());
		update.setInt(5, adress.getId());
	}

	protected void setInsert(Address adress) throws SQLException {
		insert.setString(1,adress.getCity());
		insert.setString(2,adress.getPostalCode());
		insert.setString(3,adress.getStreet());
		insert.setString(4,adress.getApNumber());
	}

	public List<Address> getPage(int offset, int limit){

		List<Address> result = new ArrayList<Address>();
		try {
			selectByPage.setInt(1, offset);
			selectByPage.setInt(2, limit);
			ResultSet rs = selectByPage.executeQuery();
			while(rs.next()){
				Address adress = new Address();
				adress = new Address();
				adress.setId(rs.getInt("id"));
				adress.setCity(rs.getString("city"));
				adress.setPostalCode(rs.getString("postalcode"));
				adress.setStreet(rs.getString("street"));
				adress.setApNumber(rs.getString("apnumber"));
				result.add(adress);
			}
		} catch (SQLException e) {
			e.printStackTrace();
		}
		return result;
	}

	public Address get(int id){
		Address adress = null;

		try {
			selectById.setInt(1, id);
			ResultSet rs = selectById.executeQuery();
			while(rs.next()){
				adress = new Address();
				adress.setId(rs.getInt("id"));
				adress.setCity(rs.getString("city"));
				adress.setPostalCode(rs.getString("postalcode"));
				adress.setStreet(rs.getString("street"));
				adress.setApNumber(rs.getString("apnumber"));
			}
		} catch (SQLException e) {
			e.printStackTrace();
		}

		return adress;
	}
}
